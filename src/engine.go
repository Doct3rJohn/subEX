func DoGetRequest(urlRequested string, userAgent string) string {
	client := &http.Client{}
	req, err := http.NewRequest("GET", urlRequested, nil)
	if err != nil {
		fmt.Println(err)
	}
	req.Header.Set("User-Agent", userAgent)

	resp, err := client.Do(req)
	if err != nil {
		fmt.Println(err)
	}
	defer resp.Body.Close()

	body, err := io.ReadAll(resp.Body)
	if err != nil {
		fmt.Println(err)
	}
	return string(body)
}

func EngineToCrawl(requestedDomain string, requestedPage int) []string {
	var res []string
	payloadList := []string {
		"https://www.google.com/search?q=site:%s+-www&start=",
		"https://www.google.com/search?q=site:%s&start=",
	}

	for i:=0; i<len(payloadList); i++ {
		dorkList := payloadList[i]
		for p:=0; p<=requestedPage; p++ {
			url := fmt.Sprintf("%s%d", dorkList, p*10)
			result := fmt.Sprintf(url, requestedDomain)
			res = append(res, result)
		}
	}
	return res
}